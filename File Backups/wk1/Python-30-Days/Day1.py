# Checking data types

print(10, type(10))                  # Int
print(3.14, type(3.14))                # Float
print("1 + 3j", (type(1 + 3j)))              # Complex
print(type('Asabeneh'))          # String
print([1, 2, 3], (type([1, 2, 3])))           # List
print(({'name':'Asabeneh'}), (type({'name':'Asabeneh'}))) # Dictionary
print(({9.8, 3.14, 2.7}), (type({9.8, 3.14, 2.7})))    # Set
print((9.8, 3.14, 2.7), (type((9.8, 3.14, 2.7))))    # Tuple